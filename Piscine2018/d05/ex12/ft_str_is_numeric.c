#include <unistd.h>
#include <stdio.h>

int	ft_str_is_numeric(char *str)
{
	int i;
	
	i = 0;
	while (str[i])
	{
		if (!(str[i] >= '0' && str[i] <= '9'))
			return (0);
		i++;
	}
	return (1);
}

int	main()
{
	char str[] = "464aa";
	
	printf("%d", ft_str_is_numeric(str));
	return (0);
}
